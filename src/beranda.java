    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            koneksi();
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from penjualan where penjualanid='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("penjualanid");
                jTextField1.setText(id);
                Date tanggal = sql.getDate("tanggalpenjualan");
                jDateChooser1.setDate(tanggal);
                String harga = sql.getString("totalharga");
                jTextField2.setText(harga);
            }
        } catch (Exception e) {}
                 

    }//GEN-LAST:event_jTable1MouseClicked

